package com.company;

public class EveryDayfromZero {
    public static void main(String[] args) {
        System.out.println("*****\n ***\n  *\n ***\n*****");
        System.out.println(2+4+5);
        System.out.println(45-32);
        System.out.println(45*32);
        int a = 11;
        double d  = 23.2;
        char c = 'D';
        System.out.println("int: " + a+"\ndouble: "+ d+"\nchar: " +c);

        int area = 2*3;
        System.out.println("The area of rectangle is: " + area);

        //print the product if the numbers
        double q = 8.2;
        int w = 6;
        System.out.println(q*w);
        // print the value of ASCII
        char c1 = 'h';
        int ascii = c1;
        System.out.println("The ASCII value of: " + c1 + " is " +ascii);

        System.out.println((int)'k');
        // double convert to int
        double d1 = 100.235;
        int x = (int) d1;
        System.out.println(x);

        double d2 = 89.99;
        System.out.println((int)d2);

    }
}
