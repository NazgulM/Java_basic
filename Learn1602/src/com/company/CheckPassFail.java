package com.company;

public class CheckPassFail {
    public static void main(String[] args) {
        int mark = 100;
        System.out.println("The mark is: " + mark);

        // if-else statement
        if (mark>=50) {
            System.out.println("Pass");
        } else {
            System.out.println("Fail");
        }
        System.out.println("Done");
    }
}
